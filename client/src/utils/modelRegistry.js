// Helper function to get model by ID
export function getModelById(id) {
  return MODEL_REGISTRY.find(model => model.id === id);
}

// Get furniture categories (exclude bathroom and structure)
export function getFurnitureCategories() {
  const allowedCategories = ["seating", "bedroom", "table", "decor", "lighting"];
  const categories = [...new Set(MODEL_REGISTRY.map(m => m.category))];
  return categories.filter(c => allowedCategories.includes(c));
}

// Get models by category
export function getModelsByCategory(category) {
  if (!category || category === 'all') {
    return MODEL_REGISTRY;
  }
  return MODEL_REGISTRY.filter(m => m.category === category);
}

// Get only furniture models (exclude bathroom and structure)
export function getFurnitureModels() {
  return MODEL_REGISTRY.filter(model =>
    ["seating", "bedroom", "table", "decor", "lighting"].includes(model.category)
  );
}